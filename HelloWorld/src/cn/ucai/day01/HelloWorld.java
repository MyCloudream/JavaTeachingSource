package cn.ucai.day01;
// ctrl + shift + F :格式化代码
// alt+shift + r 
public class HelloWorld {
	public static void main(String[] args) {
		System.out.println("hello world");
		// ctrl + / 单行注释 再按 回来
		System.out.println("hello world");
		// ctrl + shift + / 多行注释
		// ctrl + shift + \ 多行注释取消
		System.out.println("hello world");
		System.out.println("hello world");
		System.out.println("hello world");
	}
}
